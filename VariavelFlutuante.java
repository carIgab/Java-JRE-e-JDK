
public class VariavelFlutuante {
	
	public static void main(String[] args) {

		double salario = 1240.34;
		double despesas = 405.32;
		double valorRestante = salario - despesas ;
		
		System.out.println("Eu ganho " + salario +" reais por mes");
		System.out.println("Porem, Eu gasto mensalmente " + despesas + " reais");
		System.out.println("Contudo, Ainda me sobra " + valorRestante + " reais, todo mes");
}
} 